        <li>
            <strong>Community service project.</strong> In addition to earning
            the merit badges, a Scout is required to perform a community service
            project. The project should involve a minimum of eight hours of
            time, two of which must involve management planning, with the other
            six consisting of carrying out the project. It should help keep
            America beautiful and benefit the community either physically or
            financially. One way to do this is to plan the community service
            project as part of a Keep America Beautiful program. See some
            examples below.
            <ul>
                <li>
                    Plan and execute a
                    <a
                        href="https://kab.org/our-signature-programs/great-american-cleanup/"
                        >Great American Cleanup</a
                    >
                    in your local community. Adopt a park, block, beach, or
                    vacant lot. Remove litter and debris. Build picnic tables or
                    fix up old ones. Paint benches.
                </li>
                <li>
                    Build a nature trail or trashless trail for use as an
                    outdoor classroom or for community enjoyment.
                </li>
                <li>
                    Register and organize an
                    <a
                        href="https://kab.org/our-signature-programs/america-recycles-day/"
                        >America Recycles Day</a
                    >
                    event that involves your unit (i.e., newspapers, electronic
                    gadgets, and/or bottle collections).
                </li>
                <li>
                    Conduct a litter survey of local storefronts. Determine
                    areas where litter exists, and work with store owners/local
                    government to help improve conditions.
                </li>
                <li>
                    Become involved in a local environmental/recycling issue.
                    Attend public meetings, talk to public officials, and make a
                    report to your unit about what you have learned. Decide how
                    you want to become involved in resolving the issue and spend
                    your project time educating your fellow citizens as to your
                    viewpoint and assisting your “side” in its campaign.
                </li>
                <li>
                    Develop and help maintain a
                    <a
                        href="https://communitygarden.org/resources/10-steps-to-starting-a-community-garden/"
                        >community garden</a
                    >
                    or
                    <a href="http://texasforestinfo.tamu.edu/treetrails/"
                        >tree trail</a
                    >
                    in your community.
                </li>
                <li>
                    Plan and make a presentation to elementary students about
                    the importance of conserving natural resources. Take
                    students on a nature walk, pointing out natural resources.
                </li>
                <li>
                    Conduct soil and water conservation activities on a heavily
                    used trail to prevent erosion. Record your activities.
                </li>
                <li>
                    Construct water facilities and plant vegetation that will
                    provide food and shelter for wildlife in appropriate places.
                </li>
                <li>
                    Organize an anti-litter poster competition among junior and
                    senior high school students. Arrange for businesses to
                    donate awards for the winners and recruit community judges.
                    Include media coverage. Get local businesses and schools to
                    display the posters.
                </li>
            </ul>
