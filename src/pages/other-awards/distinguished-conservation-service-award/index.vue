    <h3>Distinguished Conservation Service Award</h3>

    <p>Conservation and Scouting America have been partners for a long time. Camping, hiking, and respect for the outdoors are a part of the Scouting heritage. Many of the requirements for advancement from Tenderfoot through the Eagle Scout rank call for an increasing awareness and understanding of the natural sciences. Many former Scouts have become leaders in conserving our environment and protecting it from abuse.  Right now, Scouts are involved in learning about environmental problems and actively working to make a difference.</p>

    <p>The fundamental purpose of the Scouting America Distinguished Conservation Service Awards program is to encourage learning by the participants and to increase public awareness about natural resource conservation.  Understanding and practicing sound stewardship of natural resources and environmental protection strengthens Scouting’s emphasis on respecting the outdoors. The goal of this awards program is to encourage and recognize truly outstanding efforts undertaken by Scouting units, Scouts and Venturers, adult Scouters, and other individuals, corporations, and institutions that have contributed significantly to natural resource conservation and environmental protection.</p>

    <p>The Scouting America Distinguished Conservation Service Award Individual awards are granted by the National Council through the local Council’s Conservation Committee to a member of a Scouts BSA, Sea Scout, or Venturing unit for exceptional and distinguished service to conservation and environmental improvement.</p>

    <p>The award, the most distinguished in Scouting for exceptional conservation service, will be awarded for clearly outstanding efforts in planning, leadership, execution of plans, involvement of others, and opportunities taken to help others learn about natural resource conservation and environmental improvement.</p>


    <p>To assist the applicant with the documentation needed in completing the service projects, and the documentation of the completed projects, the <a href="https://www.scouting.org/wp-content/uploads/2020/11/BSA-Distinguished-Conservation-Service-Award-Workbook-430-815.pdf">Scouting America Distinguished Conservation Service Award Project Workbook has been developed and is required to be used by the applicant</p>


    <p>It is HIGHLY recommended to contact and work with a member of the Council Conservation Committee before beginning work on this award.</p>


    <p>The Scouting America Distinguished Conservation Service Award includes a certificate, medal, and embroidered square knot for award recipients.</p>


