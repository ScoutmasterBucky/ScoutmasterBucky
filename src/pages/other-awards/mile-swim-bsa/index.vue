    <h3>Mile Swim BSA</h3>

    <p>
        Regular exercise is an important contributor to a long and healthy life.
        For example, exercise lowers several risk factors for heart disease.
        People who exercise regularly are much less likely to be overweight. Not
        only does exercise burn calories, it may actually suppress appetite.
        Blood pressure is reduced by exercise. The combination of exercise and
        weight reduction often allows people with hypertension to control their
        blood pressure without the use of medication. Smokers commonly
        discontinue that habit as they begin exercise programs. Active joggers
        tend to have lower total cholesterol than those who do not run. When it
        comes to protecting your heart, there is no substitute for exercise.
    </p>

    <p>
        Without sufficient exercise, bones become demineralized. They lose
        calcium and become brittle. Demineralization can progress rapidly in
        those who are bedridden and cannot exercise. Demineralization is a
        concern for astronauts since lack of gravity robs activity of its
        exercise value. Weak and brittle bones, caused by lack of exercise, are
        common in senior adults.
    </p>

    <p>
        Exercise is an excellent cardiovascular conditioner and important to
        living better as well as longer. People who exercise regularly feel and
        look younger than those who do not. Improvements in muscle tone and
        circulation undoubtedly contribute to freedom from fatigue and the
        feeling of well-being that the physically fit enjoy.
    </p>

    <p>
        Swimming is to some degree superior to other forms of exercise because
        it is a full-body exercise (all muscles and joints are exercised) and
        because it avoids the risk of damage to joints and other parts of the
        body that may result from activity such as running. The extensive use of
        swimming therapy for the injured and impaired is further evidence of its
        value as a body conditioner.
    </p>

    <p>
        The Mile Swim BSA recognition provides a sense of accomplishment and
        supports confidence, safety, skill, and physical fitness. The ability to
        swim long distances in warm water in time of need provides a margin of
        safety for all aquatics activities. Swimming skills and physical stamina
        are also needed to assist others. In addition to safety, Mile Swim BSA
        should encourage the development of physical fitness by introducing
        youth to swimming as regular exercise. However, it is not a competitive
        training program limited to specific strokes that emphasize speed over
        efficiency. Swimming skills, including mastery of restful strokes, are
        as important to the program as endurance. Anyone who can walk a mile
        without stopping should have sufficient stamina to swim a mile with
        restful strokes. Just as walking is a viable exercise alternative to
        jogging or sprinting, restful swimming strokes used over long distances
        in place of a fast crawl can also play a positive role in physical
        conditioning.
    </p>

    <p>
        Counselors for Mile Swim BSA must be approved by the council. Adults
        trained as a BSA Aquatics Instructor, a Red Cross Water Safety
        Instructor, or a YMCA Swim Instructor are good candidates. Swim coaches
        with a Scouting background are also appropriate. All counselors must
        have current Safe Swim Defense training.
    </p>

    <p>
        All in-water exercises must conform to Safe Swim Defense guidelines. The
        requirements specify that the participant first prepare before swimming
        a continuous mile. Four sessions of an hour each are appropriate. Each
        session should include a discussion of objectives, warm- up, stroke
        tips, and swims for increased distances. Short sprints may be included
        to illustrate different levels of aerobic exercise. The groundwork
        sessions are intended to ensure the success of all applicants who begin
        with reasonable swimming skills. The goal is encouragement and skill
        refinement, not elimination. Virtually all of those who participate in
        the training should be able to complete the mile, particularly if they
        are shown how to refine the crawl stroke using rhythmic breathing with
        the head in the water and to use alternative restful strokes such as the
        elementary backstroke, the breaststroke, and the sidestroke that utilize
        a relaxed pace and a glide.
    </p>

    <p>
        The training sessions may be conducted separately or in combination with
        another program, such as instruction for the Swimming merit badge.
        Credit may be given anyone, such as a swim-team member, who provides
        evidence of regular participation in a supervised swim training or
        fitness program that covers at least four hours over four different,
        regularly scheduled sessions.
    </p>

    <p>
        The continuous mile may be done using any combination of strokes and has
        no time limit. The swimmer may pause in deep water either by floating or
        treading water but may not rest by standing or holding to the side. Both
        open and flip turns are allowed in pools.
    </p>

    <p>
        The mile may be covered by laps in a pool or at an enclosed waterfront
        swimming area.
    </p>

    <p>
        Courses over extended distances in open water must conform to Safe Swim
        Defense guidelines regarding hazards such as submerged trees, currents,
        or boat traffic, as well as water quality, depth, and clarity.
    </p>

    <p>
        Each individual swimmer, or at most a buddy pair, may be accompanied by
        a rowboat with two people onboard, one skilled in controlling the boat
        and the other trained in basic water rescue, equipped with a reaching
        device and a flotation aid, continuously watching the swimmer(s). The
        swimmer, not the boat, must set the pace.
    </p>

    <p>
        Alternatively, a closed circuit may be established where all swimmers
        are constantly in reach of safety personnel strategically positioned at
        fixed points on anchored boats, the shore, or piers. Each participant
        swims with a buddy, and the number and spacing of those in the water
        should not exceed the capacity of the watchers to easily count the
        swimmers as they move from one zone to another.
    </p>

