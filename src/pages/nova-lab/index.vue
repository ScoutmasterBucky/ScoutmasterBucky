    <p>
        Because of how prevalent and important these fields of research are, Boy
        Scouts of America has created Nova and Supernova awards. The Cub Scout,
        Scouts BSA, Venturer, or Sea Scout can complete some of the requirements
        for rank advancement or typical progression. A little extra work will be
        rewarded with the Nova Award patch. Subsequent Nova Awards are rewarded
        with a device that's pinned to the patch.
    </p>

    <p>
        Earning a Nova award, at a very high level, looks like this for the
        different programs:
    </p>

    <p>
        <b><a href="/nova-lab/cub-scouts/">Cub Scouts</a>:</b> 1 hour of study,
        1 STEM-related adventure or equivalent, an exploration into a field, and
        discussions with a Nova counselor. Often drawings, charts, posters, or
        other visual aids are created. The Scout takes on a couple of
        experiments and reports on the findings.
    </p>

    <p>
        <b><a href="/nova-lab/scouts-bsa/">Scouts BSA</a>:</b> 3 hours of study,
        1 STEM-related merit badge, additional research into a field, some
        actual work, and discussions with a Nova counselor. Visual aids are
        often produced in the form of models, charts, drawings, presentations,
        or reports. The additional work that is performed is sufficient to have
        a good sample of that field in order to gain further understanding.
    </p>

    <p>
        <b
            ><a href="/nova-lab/venturing-and-sea-scouts/"
