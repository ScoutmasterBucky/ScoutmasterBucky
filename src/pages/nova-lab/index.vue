        <a href="explorations/">STEM-related exploration</a>, additional
        research into a field, some actual work, and discussions with a Nova
        counselor. Visual aids are often produced in the form of models, charts,
        drawings, presentations, or reports. The additional work that is
        performed is sufficient to have a good sample of that field in order to
        gain further understanding.
    </p>

