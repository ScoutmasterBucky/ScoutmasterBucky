    <h1>Welcome to Scoutmaster Bucky's Nova Lab!</h1>

    <p>
        STEM (Science, Technology, Engineering, and Math) has been a part of
        Scouts since the earliest times. Youth were encouraged to observe the
        world around them, analyze problems, experiment, and come up with
        solutions and new information. This applies to topics ranging from
        programming (technology) to model design and building (engineering). It
        even helps us understand the typical "Scouting skills" such as campfires
        (the science of chemistry) and knots (mathematics and knot theory).
    </p>

